#include "so_long.h"

int	open_fd(char **av)
{
	int	fd;

	fd = open(av[1], O_RDONLY);
	if (fd <= 0)
	{
		perror(NULL);
		exit(EXIT_FAILURE);
	}
	return (fd);
}
