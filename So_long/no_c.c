#include "so_long.h"

int	no_c(t_data *data)
{
	int	i;
	int	j;

	i = -1;
	j = -1;
	while (data->map[++j])
	{
		i = -1;
		while (data->map[j][++i])
			if (data->map[j][i] == 'C')
				return (1);
	}
	return (0);
}
