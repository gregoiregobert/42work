#include <stdlib.h>

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	char		*d;
	const char	*s;

	d = dst;
	s = src;
	if (!d && !s)
		return (0);
	while (n--)
		*d++ = *s++;
	return (dst);
}
